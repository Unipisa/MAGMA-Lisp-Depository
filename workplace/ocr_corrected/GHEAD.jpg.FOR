      SUBROUTINE GHEAD
      IMPLICIT INTEGER (A-Z)
      INTEGER*2 REDEFT
      COMMON
     */REDEFT/REDEFT /CAR/A(4),CAR(5000)
     */CDR/ADVCDR(4),CDR(5000)
     */CBLOCK/V (3),CBLOCK (4096) /LISTSC/LISTSC
     */NTYPEE/NTYPEE
      DIMENSION
     *SONBRO(4096),NREENT(4096),INF(4096)
      EQUIVALENCE
     * (CBLOCK(1), SONBRO (1)), (CBLOCK(2),NREENT(1)) ,
     * (CBLOCK(3), INF(1))
      DATA M1/Z80000000/ ,M2/Z7FFFFFFF/
      I=LISTSC
3     MAR=INF(I)/NTYPEE
      POINT=MOD(INF(I),NTYPEE)
      IF(MAR.NE.REDEFT) GOTO 1
      CALL RVLSC(POINT,OUT1,OUT2)
      IF(OUT1.EQ.0)  GOTO 1
      INF(I)=OUT2
1     INF(I)=IOR(INF (I) , M 1)
      APUNT=CAR(NREENT(I))
      IF(APUNT.LT.0) APUNT=IAND(APUNT,M2)
      CALL MABL(APUNT)
      CAR(NREENT(I))=IOR(CAR(NREENT(I)),M1)
      IF(SONBRO(I).EQ.O) GOTO 2
      I=SONBRO(I)
      GOTO 3
2     CONTINUE
      RETURN
      END
