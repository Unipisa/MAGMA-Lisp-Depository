      SUBROUTINE ALGARB
      IMPLICIT INTEGER(A-Z)
      COMMON
     */LIFE/LIFE(128)
     */UNNO/UNNO
     */NEVALV/NEVALV
     */NIL/NIL
     */ALMODE/ALMODE
     */SWIRET/SWIRET
CSTAT
CSTAT      COMMON /CONCON/CONCON
      COMMON /NUMCXT/NUECXT
C
      COMMON
      */ ABLOCK / V1(3),ABLOCK(1284)
      */ BBLOCK / V2(3),BBLOCK(3072)
      */ CBLOCK / V3(3),CBLOCK(4096)
      */ FLA / FLA
      */ FLB / FLB
      */ FLC / FLC
      */ NAPPOI / NAPPOI
      */ BPOINT / BPOINT
      */ CPOINT / CPOINT
      */ UNIT / UNIT
C
      DIMENSION
     *FUN(1284),RETP(1284),CP(1284),BLP(1284),SEGNO(1284),ABP(1284),
     *BIND(3072),VAR(3072),BBP(3072),SONBRO(4096),NREENT(4096),
     *VALORE(4096),INF(4096),LISTAR(4096),BLINK(4096)
C
      EQUIVALENCE
     *(ABLOCK(1),FUN(1)),
     *(ABLOCK(2),RETP(1)),
     *(ABLOCK(3),CP(1)),
     *(ABLOCK(4),BLP(1)),
     *(ABLOCK(5),SEGNO(1)),
     *(ABLOCK(6),ABP(1)),
     *(BBLOCK(1),BIND(1)),
     *(BBLOCK(2),VAR(1)),
     *(BBLOCK(3),BBP(1)),
     *(CBLOCK(1),SONBRO(1)),
     *(CBLOCK(2),NREENT(1)),
     *(CBLOCK(3),VALORE(1)),
     *(CBLOCK(3),INF(1)),
     *(CBLOCK(4),LISTAR(1)),
     *(CBLOCK(4),BLINK(1))
C
      CALL GETARG
       CALL MLIFE
CSTAT
CSTAT     CONCON=NUMCXT
      IF(CPOINT.NE.0) CALL MCBL(CPOINT)
      CALL GARBC
      NEVALV=NIL
      IF(ALMODE.NE.O) GOTO 151
      CALL CPOP
      SONBRO(CPOINT)=MOD(SONBRO(CPOINT),UNNO)
      GOTO 152
151   CPOINT=BLINK(CPOINT)
152   CONTINUE
      SWIRET = 2
      RETURN
      END
